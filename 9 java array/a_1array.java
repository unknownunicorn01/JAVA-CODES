public class a_1array{
  public static void main(String[] args){
    int[] arr= new int[5];  //here initilized an array
    arr[0]= 1;
    arr[1]= 2;
    arr[2]= 3;
    arr[3]= 4;
    arr[4]= 5;
    System.out.println(arr[0]);  //this will return value at that index

    // int arr[] = new int[5]; // this is another way to initilize an array
    int arr5[]={1,2,3,4}; //here we created an array
    System.out.println(arr5);  //this will return memory location
  }
}