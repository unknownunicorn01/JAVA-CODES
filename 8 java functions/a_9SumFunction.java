import java.util.*;
public class a_9SumFunction {
  public static int fun(int a, int b){
    return a+b;
  }
  public static void main(String[] args){
    Scanner sc = new Scanner(System.in);
    int a = sc.nextInt();
    int b = sc.nextInt();
    System.out.println(fun(a,b));
    sc.close();
  }
}
